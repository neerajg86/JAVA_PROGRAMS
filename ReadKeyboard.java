import java.lang.*;
import java.util.*;

public class ReadKeyboard
{	
	public static void main(String args[])
	{
		Scanner sc= new Scanner(System.in);
		int x=sc.nextInt();
		Float y= sc.nextFloat();
		String z= sc.nextLine();
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);		
	}
}